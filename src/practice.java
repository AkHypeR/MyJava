public class practice {
    public static void isEvenNumber(int n) {
        if (n % 2 == 0) {
            System.out.println(n + " is Even Number ");
        } else {
            System.out.println(n + " is odd Number ");
        }
    }

    public static void main(String[] args) {
       isEvenNumber(5);

    }
}
